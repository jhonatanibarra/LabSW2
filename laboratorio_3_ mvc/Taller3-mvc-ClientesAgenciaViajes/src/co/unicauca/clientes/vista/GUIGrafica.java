    @Override
    public void update(Observable obs, Object obj) {
        ClientesDB varClienteDB = (ClientesDB) obs;
        graficarEstadis(varClienteDB.getTotalHombres(), varClienteDB.getTotalMujeres());
    }

    private void graficarEstadis(int parMujeres, int parHombres) {
        float varTotPer = (parMujeres + parHombres);
        jLabel3.setText(String.valueOf((parMujeres * 100) / varTotPer));
        jLabel4.setText(Float.toString((parHombres * 100) / varTotPer));
        porcentajeHombres.setValue((int)((parMujeres * 100) / varTotPer));
        porcentajeMujeres.setValue((int)((parHombres * 100) / varTotPer));
    }
    
